/**
 * add - a function to return the addition of two numbers
 * @num1: the first argument
 * @num2: the second argument
 * Return: the addition of two integers
 */

int add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * sub - a function to calculate the difference betweeen two numbers
 * @num1: the first argumnt
 * @num2: the second argumnt
 * Return: the difference between two numbers
 */

int sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * mul - a function to calculate the multiplication of two numbers
 * @num1: the first argumnt
 * @num2: the second argument
 * Return: returns the multiplication of two argumnts
 */

int mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * div - a function to return the division of two numbers
 * @num1: the first argument
 * @num2: the second argument
 * Return: return the division of two numbers
 */

int div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * mod - return the modulus of two numbers
 * @num1: first argument
 * @num2: the second argument
 * Return: return the modullus of the two numbers
 */

int mod(int num1, int num2)
{
	return (num1 % num2);
}